    private void jButton_divisaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_divisaoMouseClicked
        if(div==0)
        {
            n1 = Double.parseDouble(jTextField_tela.getText());
            double verifica = Double.parseDouble("0.");
            if(n1 == verifica){
                jLabel_conta.setText("0"+" /");
                jTextField_tela.setText("0");
            }
            else
                jLabel_conta.setText(jTextField_tela.getText()+" /");
            
            div++;
            cont = 0;
        }
        if(div==1&&cont!=0){
            n2 = Double.parseDouble(jTextField_tela.getText());
            if(n1 == 0.0 && n2 == 0.0){
                jLabel_conta.setText(String.valueOf(n1)+" / "+jTextField_tela.getText()+" /");
                JOptionPane.showMessageDialog(this, "Resultado indefinido","ERROR",JOptionPane.WARNING_MESSAGE);
                jTextField_tela.setText("Resultado indefinido");
                disable = 1;
            }
            if(n1 != 0.0 && n2 == 0.0){
                jLabel_conta.setText(String.valueOf(n1)+" / ");
                JOptionPane.showMessageDialog(this, "Não é possível dividir por zero","ERROR",JOptionPane.WARNING_MESSAGE);
                jTextField_tela.setText("Não é possível dividir por zero");
                disable = 1;   
            }
            
            if(n1 != 0.0 && n2 != 0.0)
                {
                jTextField_tela.setText(String.valueOf(controller.Calculadora.subtracao(n1, n2)));
                jLabel_conta.setText(jTextField_tela.getText()+" /");
                n2 = Double.parseDouble(jTextField_tela.getText());
                cont = 0;
                result = 2;
            }
        }
    }//GEN-LAST:event_jButton_divisaoMouseClicked

