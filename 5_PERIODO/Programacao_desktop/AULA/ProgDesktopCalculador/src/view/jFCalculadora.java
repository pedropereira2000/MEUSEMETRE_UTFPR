    private void jButton_somaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_somaMouseClicked
        if(soma==0)
        {
            n1 = Double.parseDouble(jTextField_tela.getText());
            double verifica = Double.parseDouble("0.");
            if(n1 == verifica){
                jLabel_conta.setText("0"+" +");
                jTextField_tela.setText("0");
            }
            else
                jLabel_conta.setText(jTextField_tela.getText()+" +");
            
            soma++;
            cont = 0;
        }
        if(soma==1&&cont!=0){
            n2 = Double.parseDouble(jTextField_tela.getText());
            jTextField_tela.setText(String.valueOf(controller.Calculadora.soma(n1, n2)));
            jLabel_conta.setText(jTextField_tela.getText()+" +");
            n2 = Double.parseDouble(jTextField_tela.getText());
            cont = 0;
            result = 2;
        }
    }//GEN-LAST:event_jButton_somaMouseClicked

