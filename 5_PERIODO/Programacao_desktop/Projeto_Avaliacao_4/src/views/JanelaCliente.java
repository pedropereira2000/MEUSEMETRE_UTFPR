    private void jMI_cpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_cpfActionPerformed
        jTF_cpf.setText("");
        String cpf = JOptionPane.showInputDialog(this, "Informe o CPF do cliente","buscando cliente", JOptionPane.INFORMATION_MESSAGE);
        jTF_cpf.setText(cpf);
        Cliente cli = dao.ConsultarCpf(jTF_cpf.getText());
        if(cli!=null){
            JOptionPane.showMessageDialog(this, "Cliente(s) encontrado(s) com sucesso.");
            ExibeRegistro(cli);
            btn_primeiroActionPerformed(evt);
            BlockFields("search");
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum cliente foi encontrado.");
            AtualizaBaseDados();
        }
            
    }//GEN-LAST:event_jMI_cpfActionPerformed

    private void jMI_nomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_nomeActionPerformed
        nome = JOptionPane.showInputDialog(this, "Informe o NOME do cliente","buscando cliente", JOptionPane.INFORMATION_MESSAGE);
        if(endereco.equals("")&&telefone.equals("")){
            if(dao.ConsultarNome(nome)){
                JOptionPane.showMessageDialog(this, "Cliente(s) encontrado(s) com sucesso.");
                ExibeRegistro(dao.getCliente());
                btn_primeiroActionPerformed(evt);
                BlockFields("search");
            }else{
                JOptionPane.showMessageDialog(this, "Nenhum cliente foi encontrado.");
                AtualizaBaseDados();
            }
        }else if(!endereco.equals("")){
            if(dao.ConsultarAninhadaNomeEndereco(nome, endereco)){
                JOptionPane.showMessageDialog(this, "Cliente(s) encontrado(s) com sucesso.");
                ExibeRegistro(dao.getCliente());
                btn_primeiroActionPerformed(evt);
                BlockFields("search");
            }else{
                JOptionPane.showMessageDialog(this, "Nenhum cliente foi encontrado.");
                AtualizaBaseDados();
            }
        }else if(!telefone.equals("")){
            if(dao.ConsultarAninhadaNomeTelefone(nome, endereco)){
                JOptionPane.showMessageDialog(this, "Cliente(s) encontrado(s) com sucesso.");
                ExibeRegistro(dao.getCliente());
                btn_primeiroActionPerformed(evt);
                BlockFields("search");
            }else{
                JOptionPane.showMessageDialog(this, "Nenhum cliente foi encontrado.");
                AtualizaBaseDados();
            }
        }
    }//GEN-LAST:event_jMI_nomeActionPerformed

