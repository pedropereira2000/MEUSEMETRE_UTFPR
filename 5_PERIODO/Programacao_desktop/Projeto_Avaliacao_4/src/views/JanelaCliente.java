    private void btn_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inserirActionPerformed
        Cliente cli = CarregaCliente();
        if(contNew==1){
            int resp = dao.validaCliente(cli);
            if(resp == 0){
                if (dao.Inserir(cli)) {
                    JOptionPane.showMessageDialog(this, "Cliente inserido com sucesso.");
                    AtualizaBaseDados();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao inserir cliente.");
                }
            }else if(resp == 1){
                JOptionPane.showMessageDialog(this, "CPF inserido já está cadastrado.");
            }else if(resp == 2){
                JOptionPane.showMessageDialog(this, "CPF inserido é inválido.");
            }
            else if(resp == 3){
                JOptionPane.showMessageDialog(this, "Nome inserido é inválido.");
            }
            else if(resp == 4){
                JOptionPane.showMessageDialog(this, "Endereço inserido é inválido.");
            }
            else if(resp == 5){
                JOptionPane.showMessageDialog(this, "Telefone inserido é inválido.");
            }
        }else{
            jTF_cpf.setText("");
            jTF_nome.setText("");
            jTF_idade.setText("");
            jTF_fone.setText("");
            jTF_cpf.requestFocus();
            BlockFields("new");
        }
    }//GEN-LAST:event_btn_inserirActionPerformed

