    public void AtualizaBaseDados(){
        if (dao.ConsultarTodos()) {
            BlockFields("");
            if (JDBCUtil.MovPrimeiro(dao.getRsdados())) {
                ExibeRegistro(dao.getCliente());
            }
        }else{
            BlockFields("empty");
            JOptionPane.showMessageDialog(this, "Não há clientes registrados");
        }
    }
    

