    private void jMenuItem_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_excluirActionPerformed
        String rg = JOptionPane.showInputDialog(this, "Informe o RG do cliente(sem pontos e traço)","buscando cliente", JOptionPane.INFORMATION_MESSAGE);
        Cliente cli = control.getCliText(rg);
        if(cli == null){
            JOptionPane.showMessageDialog(this, "RG informado não encontrado", "ERRO DE BUSCA", JOptionPane.WARNING_MESSAGE);
        }else{
            if(JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o cliente: "+cli.getNome(),"EXCLUIR CLIENTE",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                control.excluiCliente(cli.getRg());
                ClearFields();
                JOptionPane.showMessageDialog(this, "Cliente Excluido com sucesso","AVISO",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Ação cancelada","AVISO",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jMenuItem_excluirActionPerformed

