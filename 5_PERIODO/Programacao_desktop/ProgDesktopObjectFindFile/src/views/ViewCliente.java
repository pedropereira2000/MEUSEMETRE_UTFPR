    private void jMenuItem_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_editarActionPerformed
        jButton_confirm.setVisible(true);
        jFormattedTextField_rg.setText(JOptionPane.showInputDialog(this, "Informe o RG do cliente(sem pontos e traço)","buscando cliente", JOptionPane.INFORMATION_MESSAGE));
        Cliente cli = control.getCliText(jFormattedTextField_rg.getText());
        jFormattedTextField_rg.setText("  .   .   - ");
        if(cli == null){
            JOptionPane.showMessageDialog(this, "RG informado não encontrado", "ERRO DE BUSCA", JOptionPane.WARNING_MESSAGE);
            ClearFields();
        }else{
            jTextField_nome.setText(cli.getNome());
            jFormattedTextField_rg.setText(cli.getRg());
            jTextField_endereco.setText(cli.getEndereco());
            jFormattedTextField_telefone.setText(cli.getTelefone());
            jFormattedTextField_rg.setEnabled(false);
        }
    }//GEN-LAST:event_jMenuItem_editarActionPerformed

