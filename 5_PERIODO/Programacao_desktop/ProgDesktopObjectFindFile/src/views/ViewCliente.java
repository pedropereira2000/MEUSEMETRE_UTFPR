    private void jMenuItem_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_salvarActionPerformed
        if(!jTextField_nome.getText().equals("")&&!jFormattedTextField_rg.getText().equals("  .   .   - ")&&!jTextField_endereco.getText().equals("")&&!jFormattedTextField_telefone.getText().equals("  -9    -    ")){
            control.setCliente(jTextField_nome.getText(), jFormattedTextField_rg.getText(), jTextField_endereco.getText(), jFormattedTextField_telefone.getText());
            if(!control.getTexto().equals("")){
                if(control.getCliText(jFormattedTextField_rg.getText())==null){
                    if(control.escrever(true)){
                        jTextArea_arquivoSalvo.setText(control.getTexto());
                        JOptionPane.showMessageDialog(this, "Arquivo escrito com sucesso","Escrita arquivo texto", JOptionPane.INFORMATION_MESSAGE);
                        jTextField_nome.setText("");
                        jFormattedTextField_rg.setText("  .   .   - ");
                        jTextField_endereco.setText("");
                        jFormattedTextField_telefone.setText("  -9    -    ");
                        OpemCadCli();
                    }else{
                        JOptionPane.showMessageDialog(this, "Escrita errada","Escrita arquivo texto", JOptionPane.INFORMATION_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "RG já existe","ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                if(control.escrever(true)){
                    jTextArea_arquivoSalvo.setText(control.getTexto());
                    JOptionPane.showMessageDialog(this, "Arquivo escrito com sucesso","Escrita arquivo texto", JOptionPane.INFORMATION_MESSAGE);
                    jTextField_nome.setText("");
                    jFormattedTextField_rg.setText("  .   .   - ");
                    jTextField_endereco.setText("");
                    jFormattedTextField_telefone.setText("  -9     -    ");
                    OpemCadCli();
                }else{
                    JOptionPane.showMessageDialog(this, "Escrita errada","Escrita arquivo texto", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Todos Campos devem ser preenchidos","ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem_salvarActionPerformed

