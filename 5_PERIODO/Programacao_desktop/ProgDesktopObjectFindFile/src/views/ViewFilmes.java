    private void jMenuItem_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_editarActionPerformed
        jButton_confirma.setVisible(true);
        String titulo = JOptionPane.showInputDialog(this, "Informe o TÍTULO do Filme","buscando Filmes", JOptionPane.INFORMATION_MESSAGE);
        String studio = JOptionPane.showInputDialog(this, "Informe o STUDIO que Lançou","buscando Filmes", JOptionPane.INFORMATION_MESSAGE);
        Filmes film = control.getFilmText(titulo, studio);
        if(film == null){
            JOptionPane.showMessageDialog(this, "FILME informado não encontrado!!", "ERRO DE BUSCA", JOptionPane.WARNING_MESSAGE);
            ClearFields();
        }else{
            jTextField_titulo.setText(film.getTitulo());
            jLabel_tituloAntigo.setText(film.getTitulo());
            jTextField_genero.setText(film.getGenero());
            jTextField_duracao.setText(film.getDuracao().toString());
            jComboBox_classificacao.setSelectedItem(film.getClassificacao());
            jTextField_diretor.setText(film.getStudio());
            jLabel_studioAntigo.setText(film.getStudio());
            if(film.getAlugado()==true){
                jCheckBox_filmeAlugado.setSelected(true);
            }else
                jCheckBox_filmeAlugado.setSelected(false);
            jButton_encerrar.setVisible(true);
            jButton_encerrar.setText("Cancelar Edição");
            jButton_confirma.setVisible(true);
        }
    }//GEN-LAST:event_jMenuItem_editarActionPerformed

