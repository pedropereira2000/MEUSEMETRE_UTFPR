    private void jButton_confirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_confirmaActionPerformed
        String op = "";
        if(jCheckBox_filmeAlugado.isSelected()){
            op="true";
        }else{
            op="false";
        }
        Filmes film = control.getFilmText(jTextField_titulo.getText(), jTextField_diretor.getText());
        if(film == null||(jLabel_tituloAntigo.getText().equals(jTextField_titulo.getText())&&jLabel_studioAntigo.getText().equals(jTextField_diretor.getText()))){
            control.editFilme(jLabel_tituloAntigo.getText(),jLabel_studioAntigo.getText(),jTextField_titulo.getText(), jTextField_genero.getText(), jTextField_duracao.getText(), jComboBox_classificacao.getSelectedItem().toString(), jTextField_diretor.getText(), op);
            ClearFields();
        }else{
            JOptionPane.showMessageDialog(this, "FILME já cadastrado!!", "ERRO DE BUSCA", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton_confirmaActionPerformed

    private void jMenuItem_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_excluirActionPerformed
        String titulo = JOptionPane.showInputDialog(this, "Informe o TÍTULO do Filme","buscando Filmes", JOptionPane.INFORMATION_MESSAGE);
        String studio = JOptionPane.showInputDialog(this, "Informe o STUDIO que Lançou","buscando Filmes", JOptionPane.INFORMATION_MESSAGE);
        Filmes film = control.getFilmText(titulo, studio);
        if(film == null){
            JOptionPane.showMessageDialog(this, "FILME informado não encontrado", "ERRO DE BUSCA", JOptionPane.WARNING_MESSAGE);
        }else{
            if(JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o FILME: "+film.getTitulo()+"\nProduzido por: "+film.getStudio(),"EXCLUIR FILME",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                control.excluiFilme(film.getTitulo(),film.getStudio());
                ClearFields();
                JOptionPane.showMessageDialog(this, "Filme Excluido com sucesso","AVISO",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Ação cancelada","AVISO",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jMenuItem_excluirActionPerformed

